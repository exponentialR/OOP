#include <iostream>
using namespace std;

int main () {
    // for loop
    for (int i =0; i <=5; i++){
        cout << "counting " << i << endl;
    }
    // Fibonacci
    int fibonacci[5] = {0, 1, 2, 3};
    for (double number: fibonacci){
        cout << "Fibonacci " << number << endl;
    }
}